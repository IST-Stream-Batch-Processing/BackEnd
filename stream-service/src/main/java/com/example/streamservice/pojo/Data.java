package com.example.streamservice.pojo;

public class Data {
    private String type;
    private String name;

    public Data() {
    }

    public Data(String type, String name) {
        this.type = type;
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
